#escreva um programa que, dados 2 numeros inteiros( n1 e n2), diga se eles sao iguais ou diferentes.
n1 = int(input("Digite um numero inteiro: "))
n2 = int(input("Digite um segundo numero inteiro: "))
if n1 == n2:
    print("Esses numeros sao iguais, a multiplicacao deles é: ",n1*n2)
else:
    print("Esses numeros sao diferentes, a soma deles é: ",n1+n2)


